function initHorizontalScroll(scrollContent) {
    const MAX_BASE_PROJECTS = 8;
    const REPEATS_PER_SIDE = 2;
    scrollContent.querySelectorAll('.project-spacer, .desc-spacer').forEach((el) => el.remove());
    const projectItems = Array.from(scrollContent.querySelectorAll('.project-item'));
    if (!projectItems.length) return;

    const baseItems = projectItems.slice(0, MAX_BASE_PROJECTS);
    projectItems.slice(MAX_BASE_PROJECTS).forEach((item) => item.remove());
    baseItems.forEach((item) => item.classList.remove('loop-clone'));

    const createClone = (item) => {
        const clone = item.cloneNode(true);
        clone.classList.add('loop-clone');
        clone.setAttribute('aria-hidden', 'true');
        return clone;
    };

    projectItems.forEach((item) => item.remove());

    for (let repeat = 0; repeat < REPEATS_PER_SIDE; repeat += 1) {
        baseItems.forEach((item) => {
            scrollContent.appendChild(createClone(item));
        });
    }

    baseItems.forEach((item) => {
        scrollContent.appendChild(item);
    });

    for (let repeat = 0; repeat < REPEATS_PER_SIDE; repeat += 1) {
        baseItems.forEach((item) => {
            scrollContent.appendChild(createClone(item));
        });
    }

    Array.from(scrollContent.querySelectorAll('.project-item')).forEach((item) => {
        item.style.cursor = 'pointer';
    });

    const state = {
        centeredItem: null,
        currentIndex: 0,
        isAnimating: false,
        resizeFrame: null,
        activeSlideshowItem: null,
        activeSlideshowInterval: null
    };

    const clamp = (value, min, max) => Math.min(Math.max(value, min), max);
    const allItems = () => Array.from(scrollContent.querySelectorAll('.project-item'));
    const baseSetStartIndex = baseItems.length * REPEATS_PER_SIDE;
    const getCentralEquivalentIndex = (index) => {
        const offset = ((index - baseSetStartIndex) % baseItems.length + baseItems.length) % baseItems.length;
        return baseSetStartIndex + offset;
    };

    const setCenteredIndex = (index) => {
        const items = allItems();
        if (!items.length) return;
        const nextItem = items[index];
        if (!nextItem) return;
        if (state.centeredItem && state.centeredItem !== nextItem) {
            state.centeredItem.classList.remove('is-centered');
        }
        nextItem.classList.add('is-centered');
        state.centeredItem = nextItem;
        state.currentIndex = index;
        updateCenteredSlideshow();
    };

    const stopCenteredSlideshow = () => {
        if (state.activeSlideshowInterval) {
            clearInterval(state.activeSlideshowInterval);
            state.activeSlideshowInterval = null;
        }

        if (state.activeSlideshowItem) {
            const image = state.activeSlideshowItem.querySelector('.project-image');
            const overlayImage = state.activeSlideshowItem.querySelector('.project-image-overlay');
            if (overlayImage) {
                gsap.killTweensOf(overlayImage);
                overlayImage.style.opacity = '0';
            }
            if (image && image.dataset.defaultSrc) {
                image.src = image.dataset.defaultSrc;
                image.style.opacity = '1';
            }
        }

        state.activeSlideshowItem = null;
    };

    const ensureOverlayImage = (baseImage) => {
        const wrapper = baseImage.closest('.project-image-wrapper');
        if (!wrapper) return null;

        let overlayImage = wrapper.querySelector('.project-image-overlay');
        if (!overlayImage) {
            overlayImage = baseImage.cloneNode(false);
            overlayImage.classList.add('project-image-overlay');
            overlayImage.alt = '';
            overlayImage.setAttribute('aria-hidden', 'true');
            wrapper.appendChild(overlayImage);
        }

        return overlayImage;
    };

    const swapProjectImage = (image, nextSrc, useFade = true) => {
        if (!image || !nextSrc || image.src.endsWith(nextSrc)) return;
        const overlayImage = ensureOverlayImage(image);
        if (!overlayImage) {
            image.src = nextSrc;
            return;
        }

        const applySwap = () => {
            if (!useFade) {
                image.src = nextSrc;
                image.style.opacity = '1';
                overlayImage.style.opacity = '0';
                return;
            }

            gsap.killTweensOf([image, overlayImage]);
            overlayImage.src = nextSrc;
            overlayImage.style.opacity = '0';
            gsap.to(overlayImage, {
                opacity: 1,
                duration: 0.12,
                ease: "power1.out",
                onComplete: () => {
                    image.src = nextSrc;
                    overlayImage.style.opacity = '0';
                    image.style.opacity = '1';
                }
            });
        };

        const preload = new Image();
        preload.onload = applySwap;
        preload.src = nextSrc;
    };

    const updateCenteredSlideshow = () => {
        stopCenteredSlideshow();

        const centeredItem = state.centeredItem;
        if (!centeredItem) return;

        const image = centeredItem.querySelector('.project-image');
        if (!image) return;
        if (!image.dataset.defaultSrc) {
            image.dataset.defaultSrc = image.currentSrc || image.src;
        }

        if (document.body.classList.contains('index-intro-active')) {
            swapProjectImage(image, image.dataset.defaultSrc, false);
            return;
        }

        const slideshowValue = centeredItem.dataset.slideshow;
        if (!slideshowValue) {
            swapProjectImage(image, image.dataset.defaultSrc, false);
            return;
        }

        const frames = slideshowValue.split('|').map((frame) => frame.trim()).filter(Boolean);
        if (frames.length <= 1) {
            swapProjectImage(image, frames[0] || image.dataset.defaultSrc, false);
            return;
        }

        state.activeSlideshowItem = centeredItem;
        let frameIndex = 0;
        swapProjectImage(image, frames[frameIndex], false);
        state.activeSlideshowInterval = window.setInterval(() => {
            if (state.activeSlideshowItem !== centeredItem) return;
            frameIndex = (frameIndex + 1) % frames.length;
            swapProjectImage(image, frames[frameIndex], true);
        }, 650);
    };

    window.addEventListener('indexIntroComplete', () => {
        updateCenteredSlideshow();
    });

    const getTargetXForItem = (item) => {
        if (!item) return null;
        const itemCenter = item.offsetLeft + (item.offsetWidth / 2);
        const totalTravel = Math.max(scrollContent.scrollWidth - window.innerWidth, 0);
        const minX = -totalTravel;
        return clamp((window.innerWidth / 2) - itemCenter, minX, 0);
    };

    const getLiveCenteredXForItem = (item) => {
        if (!item) return null;
        const rect = item.getBoundingClientRect();
        const itemCenter = rect.left + (rect.width / 2);
        const viewportCenter = window.innerWidth / 2;
        const currentX = Number(gsap.getProperty(scrollContent, "x"));
        const totalTravel = Math.max(scrollContent.scrollWidth - window.innerWidth, 0);
        const minX = -totalTravel;
        return clamp(currentX + (viewportCenter - itemCenter), minX, 0);
    };

    const centerItemImmediately = (item) => {
        const centeredX = getLiveCenteredXForItem(item) ?? getTargetXForItem(item);
        if (centeredX !== null) {
            gsap.set(scrollContent, { x: centeredX });
        }
    };

    const scrollItemToCenter = (itemOrIndex) => {
        const items = allItems();
        const targetIndex = typeof itemOrIndex === 'number' ? itemOrIndex : items.indexOf(itemOrIndex);
        if (targetIndex === -1) return;
        setCenteredIndex(targetIndex);
        const targetItem = items[targetIndex];
        const startX = Number(gsap.getProperty(scrollContent, "x"));
        state.isAnimating = true;
        const motion = { progress: 0 };
        gsap.to(motion, {
            progress: 1,
            duration: 0.56,
            ease: "power4.out",
            overwrite: true,
            onUpdate: () => {
                const liveTargetX = getLiveCenteredXForItem(targetItem) ?? getTargetXForItem(targetItem);
                if (liveTargetX === null) return;
                const nextX = startX + ((liveTargetX - startX) * motion.progress);
                gsap.set(scrollContent, { x: nextX });
            },
            onComplete: () => {
                const finalX = getLiveCenteredXForItem(targetItem) ?? getTargetXForItem(targetItem);
                if (finalX !== null) {
                    gsap.set(scrollContent, { x: finalX });
                }
                state.isAnimating = false;
            }
        });
    };

    const stepBy = (direction) => {
        if (state.isAnimating) return;
        const items = allItems();
        if (!items.length) return;

        const minIndex = baseSetStartIndex;
        const maxIndex = baseSetStartIndex + baseItems.length - 1;

        if (direction < 0 && state.currentIndex <= minIndex) {
            const remappedIndex = state.currentIndex + baseItems.length;
            const remappedItem = items[remappedIndex];
            const previousCenteredItem = state.centeredItem;
            previousCenteredItem?.classList.add('suppress-size-transition');
            remappedItem?.classList.add('suppress-size-transition');
            setCenteredIndex(remappedIndex);
            centerItemImmediately(remappedItem);
            requestAnimationFrame(() => {
                previousCenteredItem?.classList.remove('suppress-size-transition');
                remappedItem?.classList.remove('suppress-size-transition');
            });
        } else if (direction > 0 && state.currentIndex >= maxIndex) {
            const remappedIndex = state.currentIndex - baseItems.length;
            const remappedItem = items[remappedIndex];
            const previousCenteredItem = state.centeredItem;
            previousCenteredItem?.classList.add('suppress-size-transition');
            remappedItem?.classList.add('suppress-size-transition');
            setCenteredIndex(remappedIndex);
            centerItemImmediately(remappedItem);
            requestAnimationFrame(() => {
                previousCenteredItem?.classList.remove('suppress-size-transition');
                remappedItem?.classList.remove('suppress-size-transition');
            });
        }

        const nextIndex = state.currentIndex + direction;
        if (nextIndex < 0 || nextIndex >= items.length) return;
        scrollItemToCenter(nextIndex);
    };

    allItems().forEach((item) => {
        item.addEventListener('click', () => {
            const projectUrl = item.dataset.projectUrl;
            if (projectUrl) {
                startProjectCardTransition(item, projectUrl);
                return;
            }
            scrollItemToCenter(item);
        });
    });

    requestAnimationFrame(() => {
        const items = allItems();
        state.currentIndex = baseSetStartIndex;
        const initialItem = items[state.currentIndex];
        if (initialItem) {
            initialItem.classList.add('suppress-size-transition');
            setCenteredIndex(state.currentIndex);
            centerItemImmediately(initialItem);
            requestAnimationFrame(() => {
                initialItem.classList.remove('suppress-size-transition');
            });
        }
    });

    window.addEventListener("resize", () => {
        if (state.resizeFrame) {
            cancelAnimationFrame(state.resizeFrame);
        }
        state.resizeFrame = requestAnimationFrame(() => {
            const items = allItems();
            const currentItem = items[state.currentIndex];
            if (!currentItem) return;
            currentItem.classList.add('suppress-size-transition');
            setCenteredIndex(state.currentIndex);
            centerItemImmediately(currentItem);
            requestAnimationFrame(() => {
                currentItem.classList.remove('suppress-size-transition');
            });
        });
    });

    const loopTrigger = {
        disable() {},
        enable() {}
    };

    return {
        loopTrigger,
        updateCenteredCarouselItem: () => setCenteredIndex(state.currentIndex),
        scrollItemToCenter,
        stepBy
    };
}

function initLabScroll(scrollContent) {
    const container = document.querySelector('.lab-scroll-container');
    if (!container) return;

    const state = {
        currentX: 0,
        targetX: 0
    };

    const getMinX = () => Math.min(window.innerWidth - scrollContent.scrollWidth, 0);
    const clampX = (value) => Math.max(getMinX(), Math.min(0, value));
    const syncState = () => {
        state.currentX = clampX(Number(gsap.getProperty(scrollContent, 'x')) || 0);
        state.targetX = state.currentX;
        gsap.set(scrollContent, { x: state.currentX });
    };

    syncState();

    const animateToTarget = () => {
        gsap.to(state, {
            currentX: state.targetX,
            duration: 0.85,
            ease: 'power3.out',
            overwrite: true,
            onUpdate: () => {
                gsap.set(scrollContent, { x: state.currentX });
            }
        });
    };

    const stepScroll = (delta) => {
        if (document.body.classList.contains('play-intro-active')) return;
        state.targetX = clampX(state.targetX - delta);
        animateToTarget();
    };

    container.addEventListener('wheel', (event) => {
        const dominantDelta = Math.abs(event.deltaX) > Math.abs(event.deltaY) ? event.deltaX : event.deltaY;
        if (Math.abs(dominantDelta) < 4) return;
        event.preventDefault();
        stepScroll(dominantDelta * 1.2);
    }, { passive: false });

    window.addEventListener('resize', () => {
        syncState();
    });
}

function initListView(carouselState) {
    const scrollContainer = document.querySelector('.scroll-container');
    const scrollContent = document.getElementById('index-scroll-content');
    const listView = document.getElementById('project-list-view');
    const textCloud = document.getElementById('project-text-cloud');
    const previewPanel = document.getElementById('list-preview-panel');
    const toggleCarousel = document.getElementById('toggle-carousel');
    const toggleList = document.getElementById('toggle-list');
    const toggleCarouselControl = toggleCarousel?.closest('.nav-link-wrapper');
    const toggleListControl = toggleList?.closest('.nav-link-wrapper');

    if (!scrollContainer || !scrollContent || !listView || !textCloud || !previewPanel || !toggleCarouselControl || !toggleListControl) {
        return;
    }

    // Extract project data
    const projects = Array.from(scrollContent.querySelectorAll('.project-item:not(.loop-clone)')).map(item => ({
        title: item.querySelector('.project-title').textContent,
        img: item.querySelector('.project-image').src
    }));

    const projectImages = [];

    // Populate text cloud and preview stack
    projects.forEach((project, index) => {
        const span = document.createElement('span');
        span.className = 'list-item';
        span.textContent = project.title + (index < projects.length - 1 ? ',' : '');

        const img = document.createElement('img');
        img.className = 'preview-image';
        img.src = project.img;
        img.dataset.baseZ = String(projects.length - index);
        img.style.zIndex = img.dataset.baseZ;

        const rotation = 0;
        const tx = (Math.random() - 0.5) * 200;
        const ty = (Math.random() - 0.5) * 200;
        img.style.transform = `translate(${tx}px, ${ty}px) rotate(${rotation}deg)`;

        previewPanel.appendChild(img);
        projectImages.push(img);

        span.addEventListener('mouseenter', () => {
            if (!isListInteractive) return;
            projectImages.forEach((image) => {
                image.classList.remove('top-image');
                image.style.zIndex = image.dataset.baseZ || '1';
            });
            img.classList.add('top-image');
            img.style.zIndex = String(projects.length + 10);

            textCloud.querySelectorAll('.list-item').forEach((item) => item.classList.remove('selected'));
            span.classList.add('selected');
        });

        textCloud.appendChild(span);
    });

    let listScrollY = 0;
    let isViewTransitioning = false;
    let isListInteractive = false;
    const carouselExitDuration = 0.44;
    const carouselExitStagger = 0.035;
    const carouselEnterDuration = 0.62;
    const carouselEnterStagger = 0.04;
    const previewEnterDuration = 0.6;
    const previewImageStagger = 0.08;
    const textRevealDuration = 0.62;
    const textRevealStagger = 0.035;

    const getVisibleCarouselItems = () => {
        return Array.from(scrollContent.querySelectorAll('.project-item')).filter((item) => {
            const rect = item.getBoundingClientRect();
            return rect.right > 0 && rect.left < window.innerWidth;
        }).sort((a, b) => a.getBoundingClientRect().left - b.getBoundingClientRect().left);
    };

    const resetCarouselItemOffsets = () => {
        const allItems = scrollContent.querySelectorAll('.project-item');
        gsap.killTweensOf(allItems);
        gsap.set(allItems, { clearProps: "x" });
    };

    const animateCarouselItemsOut = (items) => {
        if (!items.length) return;
        gsap.killTweensOf(items);
        gsap.to(items, {
            y: 80,
            opacity: 0,
            duration: carouselExitDuration,
            ease: "power2.inOut",
            stagger: carouselExitStagger,
            overwrite: true
        });
    };

    const animateCarouselItemsIn = (items) => {
        if (!items.length) return;
        gsap.killTweensOf(items);
        gsap.set(items, {
            y: 80,
            opacity: 0
        });
        gsap.to(items, {
            y: 0,
            opacity: 1,
            duration: carouselEnterDuration,
            ease: "power3.out",
            stagger: carouselEnterStagger,
            overwrite: true,
            clearProps: "x,y,opacity"
        });
    };

    const setIndexOverflowLock = () => {
        document.body.style.overflow = 'hidden';
    };

    // View Switching Logic
    toggleListControl.addEventListener('click', () => {
        if (isViewTransitioning || listView.classList.contains('active-view')) return;
        isViewTransitioning = true;
        isListInteractive = false;
        if (carouselState?.loopTrigger) {
            carouselState.loopTrigger.disable();
        }
        resetCarouselItemOffsets();
        const visibleCarouselItems = getVisibleCarouselItems();

        toggleCarouselControl.classList.remove('active-footer');
        toggleListControl.classList.add('active-footer');
        listView.classList.add('active-view');
        listScrollY = 0;
        gsap.set(textCloud, { y: 0 });
        const items = textCloud.querySelectorAll('.list-item');
        projectImages.forEach((image) => image.classList.remove('top-image'));
        items.forEach((item) => item.classList.remove('selected'));
        projectImages.forEach((image) => {
            image.style.transition = 'none';
            image.classList.add('stacked');
        });

        gsap.killTweensOf([visibleCarouselItems, previewPanel, projectImages, textCloud, items]);
        gsap.set(previewPanel, { opacity: 1, x: 0, scale: 1 });
        gsap.set(projectImages, {
            opacity: 1,
            x: 0,
            y: () => window.innerHeight - previewPanel.getBoundingClientRect().top + 220,
            scale: 1
        });
        gsap.set(textCloud, { opacity: 0, y: 36 });
        gsap.set(items, { opacity: 0, y: 24 });

        const tl = gsap.timeline({
            onComplete: () => {
                projectImages.forEach((image) => {
                    image.style.transition = '';
                });
                isListInteractive = true;
                isViewTransitioning = false;
            }
        });

        tl.add(() => animateCarouselItemsOut(visibleCarouselItems), 0)
        .to(projectImages, {
            y: 0,
            duration: 0.58,
            ease: "none",
            stagger: previewImageStagger
        }, 0.12)
        .add(() => {
            scrollContainer.classList.add('hidden-view');
            resetCarouselItemOffsets();
        }, carouselExitDuration + (Math.max(visibleCarouselItems.length - 1, 0) * carouselExitStagger))
        .to(textCloud, {
            opacity: 1,
            y: 0,
            duration: 0.42,
            ease: "power2.out"
        }, 0.48)
        .to(items, {
            opacity: 1,
            y: 0,
            duration: textRevealDuration,
            stagger: textRevealStagger,
            ease: "power3.out"
        }, 0.54);

        setIndexOverflowLock();
    });

    // List Scrolling Logic
    listView.addEventListener('wheel', (e) => {
        if (!listView.classList.contains('active-view')) return;
        if (Math.abs(e.deltaY) < 5) return;

        const visibleHeight = window.innerWidth * 0.22;
        const listHeight = textCloud.offsetHeight;
        if (listHeight <= visibleHeight) return;

        listScrollY -= e.deltaY * 0.8;

        const minScroll = -(listHeight - visibleHeight);
        const maxScroll = 0;
        if (listScrollY > maxScroll) listScrollY = maxScroll;
        if (listScrollY < minScroll) listScrollY = minScroll;

        gsap.to(textCloud, {
            y: listScrollY,
            duration: 0.4,
            ease: "power2.out",
            overwrite: true
        });
    }, { passive: true });

    toggleCarouselControl.addEventListener('click', () => {
        if (isViewTransitioning || !listView.classList.contains('active-view')) return;
        isViewTransitioning = true;
        isListInteractive = false;
        const items = textCloud.querySelectorAll('.list-item');

        gsap.killTweensOf([previewPanel, projectImages, textCloud, items]);
        gsap.timeline({
            onComplete: () => {
                resetCarouselItemOffsets();
                scrollContainer.classList.remove('hidden-view');
                const visibleCarouselItems = getVisibleCarouselItems();
                animateCarouselItemsIn(visibleCarouselItems);
                listView.classList.remove('active-view');
                toggleListControl.classList.remove('active-footer');
                toggleCarouselControl.classList.add('active-footer');
                setIndexOverflowLock();
                listScrollY = 0;
                gsap.set(textCloud, { y: 0, opacity: 0 });
                gsap.set(previewPanel, { x: 0, scale: 1, opacity: 0 });
                gsap.set(projectImages, { x: 0, y: 24, scale: 0.985, opacity: 0 });
                items.forEach((item) => item.classList.remove('selected'));
                projectImages.forEach((image) => image.classList.remove('top-image'));
                carouselState?.loopTrigger?.enable(true);
                carouselState?.updateCenteredCarouselItem?.();
                gsap.delayedCall(carouselEnterDuration + (Math.max(visibleCarouselItems.length - 1, 0) * carouselEnterStagger), () => {
                    ScrollTrigger.refresh();
                    carouselState?.updateCenteredCarouselItem?.();
                    isViewTransitioning = false;
                });
            }
        })
        .to(items, {
            opacity: 0,
            y: 20,
            duration: 0.26,
            stagger: 0.012,
            ease: "power2.in"
        }, 0)
        .to(textCloud, {
            opacity: 0,
            y: 24,
            duration: 0.28,
            ease: "power2.in"
        }, 0.02)
        .to(projectImages, {
            opacity: 0,
            y: 18,
            duration: 0.3,
            stagger: 0.02,
            ease: "power2.in"
        }, 0)
        .to(previewPanel, {
            opacity: 0,
            x: -18,
            scale: 0.98,
            duration: 0.34,
            ease: "power2.in"
        }, 0.02);
    });
}

function initLabModal() {
    const modal = document.getElementById('lab-modal');
    if (!modal) return;

    const modalImageContainer = document.getElementById('modal-image-container');
    const modalKicker = document.getElementById('modal-kicker');
    const modalTitle = document.getElementById('modal-title');
    const modalDescription = document.getElementById('modal-description');
    const thumbs = document.querySelectorAll('.lab-thumb');
    const metadata = document.querySelector('.modal-metadata');

    let currentGallery = [];
    let galleryIndex = 0;
    let isTransitioning = false;
    let activeVideoEmbed = null;

    const createVideoFrame = (src) => {
        const frame = document.createElement('iframe');
        frame.className = 'modal-media-frame';
        frame.src = src;
        frame.allow = 'autoplay; fullscreen; picture-in-picture; clipboard-write; encrypted-media';
        frame.allowFullscreen = true;
        frame.setAttribute('title', 'Play media');
        return frame;
    };

    const getThumbMetadata = (thumb) => {
        const groupLabel = thumb.closest('.lab-group')?.querySelector('.group-label')?.textContent?.trim() || 'Play Archive';
        const img = thumb.querySelector('img');
        return {
            kicker: thumb.getAttribute('data-project-kicker') || groupLabel,
            title: thumb.getAttribute('data-project-title') || img?.alt || 'Play Archive',
            description: thumb.getAttribute('data-project-description') || 'An experimental study from the Play archive.'
        };
    };

    const applyThumbMetadata = (thumb) => {
        const meta = getThumbMetadata(thumb);
        if (modalKicker) modalKicker.textContent = meta.kicker;
        if (modalTitle) modalTitle.textContent = meta.title;
        if (modalDescription) modalDescription.textContent = meta.description;
    };

    thumbs.forEach((thumb) => {
        const hoverVideoSrc = thumb.getAttribute('data-video-hover');
        if (!hoverVideoSrc || thumb.classList.contains('lab-thumb-placeholder')) return;

        const previewFrame = createVideoFrame(hoverVideoSrc);
        previewFrame.classList.add('lab-thumb-media-preview');
        thumb.appendChild(previewFrame);
        requestAnimationFrame(() => {
            thumb.classList.add('is-video-previewing');
        });
    });

    thumbs.forEach(thumb => {
        thumb.addEventListener('click', () => {
            const labScrollContainer = thumb.closest('.lab-scroll-container');
            if (labScrollContainer?.dataset.justDragged === 'true') {
                return;
            }
            const img = thumb.querySelector('img');
            const galleryData = thumb.getAttribute('data-gallery');
            const videoEmbed = thumb.getAttribute('data-video-embed');
            if (img) {
                // 1. Capture Position & Setup Gallery
                const rect = img.getBoundingClientRect();
                currentGallery = videoEmbed ? [] : (galleryData ? galleryData.split(',') : [img.src]);
                galleryIndex = 0;
                activeVideoEmbed = videoEmbed;
                applyThumbMetadata(thumb);

                // 2. Setup Modal Image
                modalImageContainer.innerHTML = '';
                const clonedImg = img.cloneNode(true);
                clonedImg.style.width = '100%';
                clonedImg.style.height = '100%';
                modalImageContainer.appendChild(clonedImg);
                
                // 3. Reset Metadata state for consistent spawn
                gsap.set(metadata, { opacity: 0, y: 30 });

                // 4. Initial "Fly" state
                const targetRect = modalImageContainer.getBoundingClientRect();
                
                gsap.set(modalImageContainer, {
                    position: 'fixed',
                    top: rect.top,
                    left: rect.left,
                    width: rect.width,
                    height: rect.height,
                    borderRadius: '2px',
                    margin: 0,
                    zIndex: 2100
                });

                // 5. Show Modal & Fly
                modal.classList.add('active');
                document.body.style.overflow = 'hidden';

                gsap.to(modalImageContainer, {
                    top: targetRect.top,
                    left: targetRect.left,
                    width: targetRect.width,
                    height: targetRect.height,
                    duration: 1.5,
                    ease: "power4.inOut",
                    clearProps: "all",
                    onComplete: () => {
                        if (activeVideoEmbed) {
                            modalImageContainer.innerHTML = '';
                            modalImageContainer.appendChild(createVideoFrame(activeVideoEmbed));
                        }
                        gsap.fromTo(metadata, 
                            { opacity: 0, y: 30 }, 
                            { opacity: 1, y: 0, duration: 1.0, ease: "power2.out" }
                        );
                    }
                });
            }
        });
    });

    const updateGalleryImage = (direction) => {
        if (activeVideoEmbed || isTransitioning || currentGallery.length <= 1) return;
        isTransitioning = true;

        galleryIndex = (galleryIndex + direction + currentGallery.length) % currentGallery.length;
        
        const oldImg = modalImageContainer.querySelector('img');
        const newImg = new Image();
        newImg.src = currentGallery[galleryIndex];
        newImg.style.position = 'absolute';
        newImg.style.top = '0';
        newImg.style.left = '0';
        newImg.style.width = '100%';
        newImg.style.height = '100%';
        newImg.style.objectFit = 'cover';
        newImg.style.opacity = '0';
        
        modalImageContainer.appendChild(newImg);

        gsap.to(oldImg, { opacity: 0, duration: 0.6, ease: "power2.inOut" });
        gsap.to(newImg, { 
            opacity: 1, 
            duration: 0.6, 
            ease: "power2.inOut",
            onComplete: () => {
                oldImg.remove();
                isTransitioning = false;
            }
        });
    };

    // Scroll to switch gallery images
    modal.addEventListener('wheel', (e) => {
        if (!modal.classList.contains('active')) return;
        if (Math.abs(e.deltaY) > 20) {
            updateGalleryImage(e.deltaY > 0 ? 1 : -1);
        }
    }, { passive: true });

    const closeModal = () => {
        gsap.to(modal, {
            opacity: 0,
            duration: 0.4,
            onComplete: () => {
                modal.classList.remove('active');
                gsap.set(modal, { clearProps: "opacity" });
                gsap.set(metadata, { opacity: 0, y: 30 }); // Hard reset
                modalImageContainer.innerHTML = '';
                currentGallery = [];
                activeVideoEmbed = null;
                document.body.style.overflow = 'auto';
            }
        });
    };

    modal.addEventListener('click', (event) => {
        if (!modal.classList.contains('active')) return;
        if (event.target.closest('.modal-image-container') || event.target.closest('.modal-metadata')) return;
        closeModal();
    });
}

function initMobileLabDrag(labStrip) {
    const scrollContainer = labStrip.closest('.lab-scroll-container');
    if (!scrollContainer) return;

    let isDragging = false;
    let activePointerId = null;
    let startX = 0;
    let startScrollLeft = 0;
    let moved = false;

    const clearDragFlag = () => {
        delete scrollContainer.dataset.justDragged;
    };

    const onPointerMove = (event) => {
        if (!isDragging || event.pointerId !== activePointerId) return;

        const deltaX = event.clientX - startX;
        if (Math.abs(deltaX) > 4) {
            moved = true;
            scrollContainer.dataset.justDragged = 'true';
        }

        event.preventDefault();
        scrollContainer.scrollLeft = startScrollLeft - deltaX;
    };

    const endDrag = (event) => {
        if (event && event.pointerId !== activePointerId) return;
        if (!isDragging) return;
        isDragging = false;
        activePointerId = null;
        scrollContainer.classList.remove('is-dragging');

        if (moved) {
            window.setTimeout(clearDragFlag, 180);
        } else {
            clearDragFlag();
        }
    };

    scrollContainer.addEventListener('pointerdown', (event) => {
        if (event.pointerType === 'mouse' && event.button !== 0) return;
        isDragging = true;
        activePointerId = event.pointerId;
        moved = false;
        startX = event.clientX;
        startScrollLeft = scrollContainer.scrollLeft;
        scrollContainer.classList.add('is-dragging');
        scrollContainer.setPointerCapture(event.pointerId);
    });

    scrollContainer.addEventListener('pointermove', onPointerMove);
    scrollContainer.addEventListener('pointerup', endDrag);
    scrollContainer.addEventListener('pointercancel', endDrag);
    scrollContainer.addEventListener('pointerleave', endDrag);
}

function initMobileLabLabelTracking(labStrip) {
    const scrollContainer = labStrip.closest('.lab-scroll-container');
    if (!scrollContainer) return;

    const labels = Array.from(labStrip.querySelectorAll('.lab-group .group-label'));
    if (!labels.length) return;

    let rafId = null;

    const updatePositions = () => {
        rafId = null;
        const viewportCenter = window.innerWidth / 2;

        labels.forEach((label) => {
            const group = label.closest('.lab-group');
            if (!group) return;

            const groupRect = group.getBoundingClientRect();
            const header = label.closest('.lab-group-header');
            const headerWidth = header ? header.clientWidth : groupRect.width;
            const labelWidth = label.offsetWidth;
            const desiredLeft = viewportCenter - groupRect.left - (labelWidth / 2);
            const maxLeft = Math.max(0, headerWidth - labelWidth);
            const clampedLeft = Math.max(0, Math.min(maxLeft, desiredLeft));

            label.style.transform = `translate3d(${clampedLeft}px, 0, 0)`;
        });
    };

    const requestUpdate = () => {
        if (rafId !== null) return;
        rafId = requestAnimationFrame(updatePositions);
    };

    updatePositions();
    scrollContainer.addEventListener('scroll', requestUpdate, { passive: true });
    window.addEventListener('resize', requestUpdate);
}
