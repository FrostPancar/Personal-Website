    function updateClock() {
        const clockEl = document.getElementById('cst-clock');
        if (!clockEl) return;

        // Get current time in CST (America/Chicago)
        const now = new Date();
        const options = { timeZone: 'America/Chicago', hour: '2-digit', minute: '2-digit', hour12: false };
        const timeString = new Intl.DateTimeFormat('en-US', options).format(now);
        
        // timeString might come out like "14:30" or "24:00" depending on format, split it 
        const parts = timeString.split(':');
        if (parts.length === 2) {
            clockEl.innerHTML = `${parts[0]}<span class="blink">:</span>${parts[1]}`;
        }
    }
    
    // Update every second
    setInterval(updateClock, 1000);
